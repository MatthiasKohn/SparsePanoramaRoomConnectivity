"""
exp33 — Flip-prior DIAGNOSTIC (follow-up to exp29's negative result).

exp29 (tag hn, 31 floors of the 20 held-out cyclic homes) returned embedding flip
accuracy ~0.45 — BELOW chance (0.50) and below geometry (0.55). Before rescoping the
paper around a negative, three hypotheses must be told apart, and the aggregate hid the
quantities that separate them:

  (A) SIGN / CONVENTION BUG. A below-chance prior is the classic signature. If the raw
      through-door cosine margin points CONFIDENTLY the wrong way, inverting the decision
      recovers accuracy. Test: does inverted accuracy >> 0.5? Is accuracy on the most
      CONFIDENT edges (large |margin|) far BELOW 0.5?
  (B) NO SIGNAL / DEPTH-LIMITED. The prior is genuinely uninformative: margins tiny,
      accuracy ~0.5 at every confidence level. Gating cannot help; the mechanism (or the
      DAP depth it rides on) is the problem.
  (C) GATING-RECOVERABLE. The prior is right when |margin| (or inlier) is large and random
      when small. Reporting the high-confidence subset recovers a usable signal.

For every door edge on every depth-equipped floor this dumps the RAW quantities:
  the two through-door reprojection cosine scores (direct vs flipped side), their margin,
  the prior's choice, the GT true side, the registration inlier, bridge/cycle.
Then it reports prior accuracy, INVERTED accuracy (bug test), accuracy stratified by
|margin| and by inlier (gating test), and a per-home breakdown. No new data needed — it
reuses the DAP depth already generated by trackD.

  python experiments/exp33_flip_diagnose.py --root $ZIND_ROOT \
      --homes scripts/depth_homes.txt --ckpt $RUN_ROOT/hardneg/best.pt --device cuda --tag hn
"""
import sys, os, argparse, csv
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
from pathlib import Path
import numpy as np

import config
from src import door_pose, panoproj, posegraph as pg
from experiments.exp27_hybrid_real import make_prov, door_edges, largest_cc
from experiments.exp18_floor_graph_real import (load_pano, gt2d, get_embedder,
                                                _reproj_centroid_az_deg, FOV)
from experiments.exp29_floor_benchmark import bridge_mask, floors_of


def emb_scores(embed, panoA, panoB, depthA, az_a_deg, m, mflip):
    """Return (cos_direct, cos_flip): A's through-door crop vs B-crops at the reprojected
    centroid under each candidate pose. Mirrors exp18.side_pref_embedding but exposes the
    raw scores instead of only the argmax."""
    H, W = depthA.shape
    P, _ = door_pose.through_door_points(depthA, az_a_deg, W, H)
    if P is None or len(P) < 30:
        return None
    cropA = panoproj.e2p(panoA, az_a_deg, 0.0, FOV, (224, 224))
    fA = embed(cropA); fA = fA / (np.linalg.norm(fA) + 1e-8)
    sc = []
    for cm in (m, mflip):
        azc = _reproj_centroid_az_deg(P, cm)
        cropB = panoproj.e2p(panoB, azc, 0.0, FOV, (224, 224))
        fB = embed(cropB); fB = fB / (np.linalg.norm(fB) + 1e-8)
        sc.append(float(fA @ fB))
    return sc[0], sc[1]


def diag_floor(prov, embed, name, oracle_pose=False):
    """One row per door edge on the floor's largest connected component.

    oracle_pose: instead of the DAP-depth-estimated relative pose, compute the
    through-door reprojection under the GT relative pose (and its point-reflection).
    This removes ALL pose/depth error from the reprojection step, so the resulting
    accuracy isolates a single question: given perfect geometry, does through-door
    APPEARANCE carry the which-side signal at all? (~chance => mechanism is dead, not
    just depth-limited; high => the failure is pose/depth, fixable with better depth.)
    """
    de = door_edges(prov)
    if len(de) < 2:
        return []
    roomof = lambda s: prov.fl.panos[s]["room"]
    edge_rooms = [(roomof(x), roomof(y)) for x, y, _, _ in de]
    keep = largest_cc(edge_rooms)
    rep = {}
    for (x, y, _, _) in de:
        rep.setdefault(roomof(x), x); rep.setdefault(roomof(y), y)
    de = [t for t, (ra, rb) in zip(de, edge_rooms) if ra in keep and rb in keep]
    edge_rooms = [(ra, rb) for (ra, rb) in edge_rooms if ra in keep and rb in keep]
    stems = sorted(keep); idx = {r: k for k, r in enumerate(stems)}; N = len(stems)
    if N < 3 or len(de) < 2:
        return []
    Xgt = np.array([gt2d(prov, rep[r]) for r in stems])

    edges, recs, pim = [], [], {}
    for (x, y, inl, e), (ra, rb) in zip(de, edge_rooms):
        i, j = idx[ra], idx[rb]
        m = e["m"]; mflip = pg.rot_pi_about(m, e["doorA"])
        edges.append((i, j))
        mgt = pg.between(Xgt[i], Xgt[j])
        if oracle_pose:
            m_use, mflip_use, ti = mgt, pg.rot_pi_about(mgt, e["doorA"]), 0
        else:
            m_use, mflip_use = m, mflip
            ti = 0 if np.linalg.norm((m - mgt)[:2]) <= np.linalg.norm((mflip - mgt)[:2]) else 1
        for s in (x, y):
            if s not in pim:
                pim[s] = load_pano(prov, s)
        sc = emb_scores(embed, pim[x], pim[y], prov.depth(x),
                        np.degrees(e["az_a"]), m_use, mflip_use)
        rec = dict(home=name, pano_a=x, pano_b=y, room_a=ra, room_b=rb,
                   inlier=round(float(inl), 4), true_idx=int(ti))
        if sc is None:
            rec.update(valid=0, sc_direct="", sc_flip="", margin="",
                       emb_pref=-1, emb_correct="")
        else:
            s0, s1 = sc
            pref = 0 if s0 >= s1 else 1
            rec.update(valid=1, sc_direct=round(s0, 5), sc_flip=round(s1, 5),
                       margin=round(s0 - s1, 5), emb_pref=pref,
                       emb_correct=int(pref == ti))
        recs.append(rec)
    for rec, b in zip(recs, bridge_mask(N, edges)):
        rec["bridge"] = int(b)
    return recs


def _acc(sub):
    return float(np.mean([r["emb_correct"] for r in sub])) if sub else float("nan")


def _gated(valid, key, absval=False):
    """Accuracy over the top {100,50,30,15}% edges ranked by `key` (desc)."""
    kv = (lambda r: abs(r[key])) if absval else (lambda r: r[key])
    order = sorted(valid, key=kv, reverse=True)
    out = {}
    for frac in (1.00, 0.50, 0.30, 0.15):
        k = max(1, int(round(frac * len(order))))
        out[frac] = (_acc(order[:k]), k)
    return out


def main(a):
    root = Path(a.root)
    home_ids = Path(a.homes).read_text().split() if Path(a.homes).exists() \
        else a.homes.split(",")
    embed = get_embedder(a.ckpt or a.embed, device=a.device)

    rows = []
    for hid in home_ids:
        home = root / hid
        depth_dir = Path(a.depth_sub) if os.path.isabs(a.depth_sub) else home / a.depth_sub
        if not depth_dir.exists():
            print(f"{hid}: no depth at {depth_dir} — skipped"); continue
        for floor in floors_of(home):
            try:
                prov = make_prov(str(home), str(depth_dir), floor)
                have = [n for n in prov.fl.panos if (prov.depth_dir / f"{n}.npy").exists()]
                if len(have) < 3:
                    continue
                fr = diag_floor(prov, embed, f"{hid}/{floor}", oracle_pose=a.oracle_pose)
            except Exception as e:
                print(f"{hid}/{floor}: FAILED — {e}"); continue
            rows.extend(fr)

    valid = [r for r in rows if r["valid"] == 1]
    if not valid:
        print("no valid edges"); return

    acc = _acc(valid)
    inv = float(np.mean([1 - r["emb_correct"] for r in valid]))
    n = len(valid)
    print("=" * 74)
    print(f"edges: {len(rows)} total | {n} valid (through-door points present)")
    print(f"prior accuracy          : {acc:.3f}   (chance 0.500)")
    print(f"INVERTED-prior accuracy : {inv:.3f}   <- bug test: if this >> 0.5, sign is flipped")
    print("-" * 74)
    print("accuracy vs confidence (|cosine margin|, most-confident edges first):")
    for frac, (v, k) in _gated(valid, "margin", absval=True).items():
        print(f"  top {int(frac*100):3d}%  (n={k:3d})  acc {v:.3f}")
    print("accuracy vs registration inlier (highest inlier first):")
    for frac, (v, k) in _gated(valid, "inlier").items():
        print(f"  top {int(frac*100):3d}%  (n={k:3d})  acc {v:.3f}")
    br = [r for r in valid if r["bridge"] == 1]; cy = [r for r in valid if r["bridge"] == 0]
    print("-" * 74)
    print(f"bridge edges (n={len(br):3d})  acc {_acc(br):.3f}   |   "
          f"cycle edges (n={len(cy):3d})  acc {_acc(cy):.3f}")
    print("-" * 74)
    print("per-home (valid edges):")
    for hid in sorted({r["home"].split('/')[0] for r in valid}):
        sub = [r for r in valid if r["home"].startswith(hid + "/")]
        print(f"  {hid}: n={len(sub):2d}  acc {_acc(sub):.3f}  "
              f"mean|margin| {np.mean([abs(r['margin']) for r in sub]):.4f}")
    print("=" * 74)
    # verdict heuristic
    top15 = _gated(valid, "margin", absval=True)[0.15][0]
    if top15 < 0.40:
        print("VERDICT LEAN: (A) SIGN BUG — confident edges are confidently WRONG. "
              "Invert the emb_pref decision in exp18.side_pref_embedding and re-run exp29.")
    elif top15 > 0.65:
        print("VERDICT LEAN: (C) GATING-RECOVERABLE — confident edges are right; "
              "gate low-|margin| edges (down-weight in the pose graph) and report the subset.")
    else:
        print("VERDICT LEAN: (B) NO SIGNAL — accuracy ~chance at all confidence levels. "
              "Next: rerun with GT/cleaner depth to test the depth-limited hypothesis; "
              "else reframe the flip as an honest negative result and lean on SfM anchors (C5).")

    out = config.RESULTS_ROOT / "floorbench"; out.mkdir(parents=True, exist_ok=True)
    tag = ("_" + a.tag) if a.tag else ""
    keys = ["home", "pano_a", "pano_b", "room_a", "room_b", "bridge", "inlier",
            "true_idx", "emb_pref", "emb_correct", "sc_direct", "sc_flip", "margin", "valid"]
    with open(out / f"flipdiag{tag}.csv", "w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=keys, extrasaction="ignore")
        w.writeheader(); w.writerows(rows)
    print("saved", out / f"flipdiag{tag}.csv")


if __name__ == "__main__":
    ap = argparse.ArgumentParser()
    ap.add_argument("--root", required=True, help="ZInD full_dataset root ($ZIND_ROOT)")
    ap.add_argument("--homes", required=True, help="file with home ids OR comma-separated")
    ap.add_argument("--depth_sub", default="dap_depth/depth_meters")
    ap.add_argument("--ckpt", default=None)
    ap.add_argument("--embed", default=None, help="'selftest' for CPU plumbing check")
    ap.add_argument("--device", default="cuda")
    ap.add_argument("--oracle_pose", action="store_true",
                    help="reproject under GT relative pose -> appearance-only test")
    ap.add_argument("--tag", default="")
    a = ap.parse_args()
    main(a)
